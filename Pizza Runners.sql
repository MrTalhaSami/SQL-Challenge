------------------------------------Pizza Metrics------------------------------------------------------------------------
--(How many Pizzas Was ordered)

select count(order_id) from customer_orders;

--(how many unique customers made the order)

select count(distinct customer_id) from customer_orders;

--(How many successful orders were delivered by each runner?)

select runner_id, count(order_id) 
from runner_orders
where distance not like 'null'
group by 1
order by 1;

--(How many of each type of pizza was delivered?)
select pizza_names.pizza_name, count(customer_orders.order_id) from pizza_names
join customer_orders on pizza_names.pizza_id=customer_orders.pizza_id
join runner_orders on runner_orders.order_id=customer_orders.order_id
where distance not like 'null'
group by 1;

--(How many Vegetarian and Meatlovers were ordered by each customer?)

select customer_orders.customer_id,pizza_names.pizza_name,count(pizza_names.pizza_name) from pizza_names
join customer_orders on pizza_names.pizza_id=customer_orders.pizza_id
group by 1,2
order by 1 asc;

select customer_ORDERS.customer_id,
sum(case when exclusions <> '' or extras <> '' then 1
else 0 end) as one_change,
sum(case when exclusions ='' and extras = '' then 1 else 0 end )as no_change
from customer_orders
join runner_orders on runner_orders.order_id=customer_orders.order_id
where runner_orders.distance not like 'null'
group by 1;

--(How many pizzas were delivered that had both exclusions and extras?)
select * from customer_orders as c
join runner_orders as r on r.order_id=c.order_id
where 
r.distance is not null and 
extras is not null and 
exclusions is not null;
 
select count(order_id),
extract(hour from order_time) as houroftheday
from customer_orders
group by 2
order by 1 desc
  
SELECT 
  TO_CHAR(order_time, 'Day') AS order_day,
  COUNT(order_id) AS total_orders
FROM 
  customer_orders
GROUP BY 
  TO_CHAR(order_time, 'Day')
ORDER BY 
  total_orders DESC;
